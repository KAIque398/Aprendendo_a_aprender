a = input ("Digite qualquer coisa: ")

print ("O tipo do prompt é:", type(a)) 
print ("O prompt é númerico?", a.isnumeric())
print ("O prompt é alfabético?", a.isalpha())
print ("O prompt é alfanumérico?", a.isalnum())
print ("O prompt é somente espaços?", a.isspace())
print ("O prompt está em maiusculo?", a.isupper())
print ("O prompt está em minusculo?", a.islower())
print ("O prompt está capitalizada?", a.istitle())






